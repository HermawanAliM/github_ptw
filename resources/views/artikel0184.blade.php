    <h1>Artikel Framework PHP</h1>
    <h2>1. Laravel</h2>
    Laravel adalah salah satu Framework PHP yang paling populer dan paling banyak digunakan di seluruh dunia dalam membangun aplikasi web mulai dari proyek kecil hingga besar. Framework ini banyak digunakan oleh Web Developer karena kinerja, fitur, dan skalabilitas nya.<br>
    <p>Framework ini mengikuti struktur MVC (Model View Controller), MVC adalah sebuah metode aplikasi dengan memisahkan data dari tampilan berdasarkan komponen- komponen aplikasi, seperti : manipulasi data, controller, dan user interface.<br></p>
    <p>Dengan menggunakan struktur MVC maka membuat laravel mudah untuk dipelajari dan mempercepat proses pembuatan prototipe aplikasi web. Framework ini juga menyediakan fitur bawaan seperti otentikasi, mail, perutean, sesi, dan daftar berjalan.<br></p>
    <p>Framework ini banyak disukai oleh para web developer, Sesuai sedikit penjelasan diatas. Kelebihan lainnya yaitu Laravel sangat mudah untuk disesuaikan, Karena kemudahan itulah kamu dapat membuat suatu struktur proyek kamu sendiri yang memenuhi permintaan aplikasi web kamu.</p>

    <h2>2. CodeIgniter</h2>
    <p>CodeIgniter adalah sebuah web application network yang bersifat open source yang digunakan untuk membangun aplikasi php dinamis.</P>
    <p>CodeIgniter menjadi sebuah framework PHP dengan model MVC (Model, View, Controller) untuk membangun website dinamis dengan menggunakan PHP yang dapat mempercepat pengembang untuk membuat sebuah aplikasi web. Selain ringan dan cepat, CodeIgniter juga memiliki dokumentasi yang super lengkap disertai dengan contoh implementasi kodenya. Dokumentasi yang lengkap inilah yang menjadi salah satu alasan kuat mengapa banyak orang memilih CodeIgniter sebagai framework pilihannya. Karena kelebihan-kelebihan yang dimiliki oleh CodeIgniter, pembuat PHP Rasmus Lerdorf memuji CodeIgniter di frOSCon (Agustus 2008) dengan mengatakan bahwa dia menyukai CodeIgniter karena “it is faster, lighter and the least like a framework.”</p>
    <p>CodeIgniter pertamakali dikembangkan pada tahun 2006 oleh Rick Ellis. Dengan logo api yang menyala, CodeIgniter dengan cepat “membakar” semangat para web developer untuk mengembangkan web dinamis dengan cepat dan mudah menggunakan framework PHP yang satu ini.</p>
    </div>

    <h2>3. CakePHP</h2>
    <p>Sebelum masuk kedalam pengertian dari CakePHP alangkah baiknya kita mengetahui sejarah singkat dari framework ini. CakePHP dikembangkan oleh perusahaan yang bernama Cake Software Foundation.Inc dan memiliki rilis stabil pada versi 3.2.8 yang dikeluarkan pada 24 april 2016.</p>
    <p>CakePHP merupakan sebuah rapid development framework yang bebas digunakan oleh siapa saja. CakePHP itu sendiri adalah sebuah kerangka kerja (framework) pada bahasa pemrograman PHP yang mana digunakan untuk mempermudah developer dalam mengembangkan aplikasi berbasis web. Framework ini mendukung untuk pengembangan bergaya RAD (Rapid Application Development), yang memungkinkan untuk digunakan dan dikembangkan menjadi aplikasi lain yang lebih kompleks.</p>
    <p>CakePHP membuang semua proses monoton ketika mengembangkan sebuah aplikasi berbasis web. CakePHP menyediakan semua library yang dibutuhkan dalam mengembangkan aplikasi web dan disini developer hanya perlu fokus untuk mengembangkan logic dari aplikasinya, untuk library sudah di urus oleh framework yang satu ini.</p>
